def task_1() -> None:
    """
    Функция создает 5 переменных разных типов и выводит их типы в консоль.
    """
    # 1. Создаем переменные с разными типами данных

    # int - целое число
    my_integer = 42

    # float - дробное число
    my_float = 3.14

    # str - строка
    my_string = "Hello, World!"

    # list - список
    my_list = [1, 2, 3, "apple", "banana"]

    # bool - логическое значение
    my_boolean = True

    # 2. Выводим типы данных в консоль
    print("Типы данных переменных:")
    print(f"my_integer: {type(my_integer)}")
    print(f"my_float: {type(my_float)}")
    print(f"my_string: {type(my_string)}")
    print(f"my_list: {type(my_list)}")
    print(f"my_boolean: {type(my_boolean)}")


# c. Вызов функции
task_1()


