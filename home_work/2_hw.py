def task_3(number: int) -> int:
    """
    Функция принимает число и возвращает его квадрат.

    Args:
         number (int): Число для возведения в квадрат

    Returns:
        int: Квадрат числа
    """
    return number ** 2


print("Квадрат числа 5:", task_3(5))
print("Квадрат числа 10:", task_3(10))
print("Квадрат числа -3:", task_3(-3))
